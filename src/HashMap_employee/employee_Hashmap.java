package HashMap_employee;

public class employee_Hashmap {
	
	private int employee_ID;
	private int employee_Name;
	public int getEmployee_ID() {
		return employee_ID;
	}
	public void setEmployee_ID(int employee_ID) {
		this.employee_ID = employee_ID;
	}
	public int getEmployee_Name() {
		return employee_Name;
	}
	public void setEmployee_Name(int employee_Name) {
		this.employee_Name = employee_Name;
	}
	@Override
	public String toString() {
		return "employee_Hashmap [employee_ID=" + employee_ID + ", employee_Name=" + employee_Name + "]";
	}
	
	
	

}
